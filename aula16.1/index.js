//Arrow Function


const soma  = (s,y) =>{
    return s + y;
}
console.log(soma(7,3));

const sub = (x,y) =>{
    return x - y;
}
console.log(sub(7,3));

const multi = (x,y) =>{
    return x * y;
}

console.log(multi(7,3));

const div = (x,y) =>{
    return x & y;
}

console.log(div(7,3));